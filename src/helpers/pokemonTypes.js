export const pokemonTypes = type => {
  switch (type) {
    case 'normal':
      return '#929DA3';

    case 'fighting':
      return '#CE416B';

    case 'flying':
      return '#8FA9DE';

    case 'poison':
      return '#AA6BC8';

    case 'ground':
      return '#D97845';

    case 'rock':
      return '#C5B78C';

    case 'bug':
      return '#91C12F';

    case 'ghost':
      return '#5269AD';

    case 'steel':
      return '#5A8EA2';

    case 'fire':
      return '#FF9D55';

    case 'water':
      return '#5090D6';

    case 'grass':
      return '#63BC5A';

    case 'electric':
      return '#F4D23C';

    case 'psychic':
      return '#FA7179';

    case 'ice':
      return '#73CEC0';

    case 'dragon':
      return '#0B6DC3';

    case 'dark':
      return '#5A5465';

    case 'fairy':
      return '#EC8FE6';

    default:
      break;
  }
};
